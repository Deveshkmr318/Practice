public class LFUCache {  // Least Frequently Used

    /*  
    
    How it works: Evicts the item that has been accessed the fewest number of times.
    Similar to LRU?:
    Both track item usage, but LFU focuses on frequency rather than recency.
    Use case: When certain items are predictably accessed more often than others.
    Limitation: Requires more memory to store access frequencies and may not adapt well to changing access patterns.  
    
    */
    
}
